// 难点:简历里大量"徐州现场""确认徐州现场接受度"是招聘方对岗位地点的备注,几乎人人都有 —— 是噪声。
// 只认候选人**自身**的期望/现居/求职意向城市 = 徐州,并显式排除"徐州现场"这种招聘备注。
// 这是提示性标注(愿意来徐州但不在本地的不算),后台可点开简历复核。

// 徐州本地高校(上学地在徐州也算本地)。中国矿业大学(徐州)是 211 本部;中国矿业大学(北京)是另一所,需排除。
const XUZHOU_SCHOOLS = ["江苏师范大学", "徐州医科大学", "徐州工程学院", "江苏建筑职业技术学院", "徐州工业职业技术学院", "徐州生物工程职业技术学院", "徐州幼儿师范高等专科学校", "九州职业技术学院"];

/** 候选人是否徐州本地(期望城市 / 现居 / 上学地 = 徐州)。 */
export function detectXuzhouLocal(resumeText: string): boolean {
  const t = resumeText || "";
  // 1) 期望 / 求职意向 / 期望城市 里出现徐州(同一字段内,排除"徐州现场")
  if (/(期望|意向|求职意向|期望城市|意向城市|工作城市)[：:]?[^。;,、\n]{0,16}徐州(?!现场)/.test(t)) return true;
  // 2) 现居 / 所在地 / 人在 / 地点 / 到岗 / 户籍 / 籍贯 等紧跟徐州(排除"徐州现场")
  if (/(现居|居住地|所在地|定居|人在|目前在|现在在|户籍|籍贯|地点|到岗)[：:]?[^。;,、\n]{0,6}徐州(?!现场)/.test(t)) return true;
  // 3) 上学地在徐州:徐州本地高校,或"徐州XX大学/学院/学校"
  if (XUZHOU_SCHOOLS.some((s) => t.includes(s))) return true;
  if (/徐州[^。;,、\n]{0,6}(大学|学院|学校|职业技术学院)/.test(t)) return true;
  // 中国矿业大学本部在徐州(排除"中国矿业大学(北京)")
  if (/中国矿业大学(?!\s*[((]?\s*北京)/.test(t)) return true;
  return false;
}

// ── 所在地解析:目前所在城市(现居)+ 期望工作城市 ──
// 难点:BOSS 简历里"当前沟通职位…徐州""徐州现场"是招聘方岗位地点,不是候选人所在地 —— 用明确人物标签命中,不抓岗位备注。
// 标签按"具体→宽泛"排序,长标签在前(避免"现居"误吃"现居住地"残字)。口语化的"人在/现在在"故意不收。
const CURRENT_LABELS = ["目前所在的城市", "目前所在城市", "现所在城市", "现居住地", "现居地", "现住地", "现居", "居住地", "常驻地", "常驻", "目前所在地", "现所在地", "所在城市", "户籍所在地", "户籍", "籍贯"];
// "求职意愿"(后跟"较强/一般"程度词)不收;"期望"作宽泛兜底放最后。
const EXPECT_LABELS = ["期望城市", "意向城市", "期望工作城市", "期望工作地点", "期望工作地", "期望地点", "期望地区", "意向地点", "意向地区", "偏好工作地点", "求职意向", "期望"];

// label 与城市之间可能夹的修饰/介词词(整体跳过),如"求职意向: 期望:徐州""期望显示为成都"
const MID = "(?:期望|意向|意愿|城市|地区|地点|方向|显示|为|是|在|到|去|的|并|优先|考虑|当前|目前|及|写|可|想)";
// 城市名后常紧跟的方向/岗位/属性词("徐州算法工程师""徐州深度学习""徐州现场") —— 在其前截断
const STOP_WORDS = ["现场", "深度", "学习", "算法", "工程", "互联网", "互联", "后端", "前端", "大模型", "大模", "人工", "机器", "视觉", "智能", "数据", "开发", "方向", "相关", "行业", "岗位", "职位", "薪资", "面议", "技术", "研发", "运维", "测试", "产品", "设计", "实习", "全职", "到岗", "经验", "学历", "时间", "优先", "考虑", "不限", "为主", "及以", "或", "带货", "主播", "运营", "销售", "客服"];
// 整体以这 2~3 字开头 → 不是地名(程度词/虚词/岗位词)
const INVALID = new Set(["不限", "市区", "郊区", "城区", "家办", "居家", "远程", "暂无", "随时", "本地", "当地", "各地", "异地", "外地", "全国", "全职", "兼职", "沟通", "职位", "面议", "薪资", "方向", "行业", "岗位", "机会", "到岗", "经验", "学历", "深度", "算法", "人工", "机器", "智能", "数据", "开发", "为主", "显示", "带货", "主播", "运营", "销售", "客服", "期望", "意向", "较强", "较弱", "一般", "很强", "是个", "目前", "现在", "这个", "那个", "一个", "可以", "比较", "工作", "附近", "周边", "住地", "居地", "所在", "在地", "户籍", "籍贯"]);
// 省/自治区前缀(后面常紧跟市名,如"江苏徐州");无后缀的 3 字城市白名单
const PROVINCES = ["江苏", "山东", "浙江", "广东", "河南", "河北", "安徽", "湖南", "湖北", "四川", "福建", "江西", "云南", "贵州", "陕西", "山西", "辽宁", "吉林", "黑龙", "甘肃", "青海", "海南", "内蒙", "广西", "西藏", "宁夏", "新疆", "台湾"];
const THREE = ["连云港", "哈尔滨", "石家庄", "秦皇岛", "张家口", "驻马店", "乌鲁木", "呼和浩", "齐齐哈", "鄂尔多", "攀枝花", "景德镇", "马鞍山", "平顶山", "三门峡", "佳木斯", "牡丹江", "双鸭山", "锡林浩", "巴彦淖", "防城港", "六盘水"];

/** 把抓到的一段中文清洗成干净城市名:去前导修饰、在岗位/方向词前截断、行政后缀优先、过滤非地名词。 */
function cleanCity(raw: string): string {
  let s = (raw || "").replace(new RegExp("^" + MID + "+"), "").trim();
  if (s.length < 2) return "";
  if (INVALID.has(s.slice(0, 2)) || INVALID.has(s.slice(0, 3))) return "";
  let cut = s.length;
  for (const w of STOP_WORDS) { const i = s.indexOf(w); if (i > 0) cut = Math.min(cut, i); }
  s = s.slice(0, cut);
  if (s.length < 2) return "";
  // 行政后缀(市/区/县/州/盟)优先,截到后缀为止(支持"江苏徐州市""吴江区")
  const adm = s.match(/^[一-龥]{2,3}(?:省)?[一-龥]{0,3}(?:市|区|县|州|盟)/);
  if (adm) return INVALID.has(adm[0]) ? "" : adm[0];
  // 省份前缀:取省后面的市名(显示去省,如"江苏徐州"→"徐州")
  const prov = PROVINCES.find((p) => s.startsWith(p));
  if (prov) {
    const rest = s.slice(prov.length);
    if (rest.length >= 2) {
      const city = THREE.find((x) => rest.startsWith(x)) ? rest.slice(0, 3) : rest.slice(0, 2);
      return INVALID.has(city) ? prov : city;
    }
    return prov;
  }
  // 3 字城市白名单 / 否则取前 2 字
  const city = THREE.find((x) => s.startsWith(x)) ? s.slice(0, 3) : s.slice(0, 2);
  return city.length >= 2 && !INVALID.has(city) ? city : "";
}

function matchCity(t: string, labels: string[]): string {
  for (const lab of labels) {
    const m = t.match(new RegExp(lab + "[：:，,\\s]*" + MID + "*[：:，,\\s]*([\\u4e00-\\u9fa5]{2,8})"));
    if (!m) continue;
    const c = cleanCity(m[1]);
    if (c) return c;
  }
  return "";
}

/** 从简历文本解析候选人「目前所在城市(现居)」与「期望工作城市」。解析不到返回空串(后台可手改)。 */
export function extractLocation(resumeText: string): { current: string; expect: string } {
  const t = (resumeText || "").replace(/[ \t]+/g, " ");
  return { current: matchCity(t, CURRENT_LABELS), expect: matchCity(t, EXPECT_LABELS) };
}
